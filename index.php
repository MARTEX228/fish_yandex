            <h1>Как рыбачить в GTA 5 RP. Гайд по рыбалке для новичка.</h1>
            <b class="data">20 ноября 2020
                8,8K прочитали</b>
            <img class="img_one" src="https://avatars.dzeninfra.ru/get-zen_doc/1706869/pub_5fb822f6ffe1de7f5cef8336_5fb82336ffe1de7f5cefd707/scale_1200" alt="">
            <p>Если вы только начали играть на серверах GTA 5 RP мы поможем вам с началом рыбалки.</p>
            <p>Во первых, это самая доступная работа в GTA 5 RP, которую можно начать на первом уровне не считая стройки.</p>
            <p>Первое, что необходимо это минимальная сумма денег - 3500 удочка + наживка 35 за штуку. И желательно автомобиль - 5000.</p>
           <div class="lifehack">
               <p>Лайфхак: удочку можно купить с рук. Можно заехать на рынок или просто спросить у рыбаков на одном из пирсов. С большой доли вероятности вам продадут удочку первого уровня за 1500. Так как, опытные рыбаки, купив новую удочку, хотят избавится от старой.</p>
           </div>
            <p>Кончено лучше сначала купить автомобиль с большим багажником. Лучшим вариантом будет Ingot за 5,5 тысяч. он совмещает себе цену и большой багажник 90 кг (вы увезете много рыбы за раз)</p>
            <img class="img_content" src="https://avatars.dzeninfra.ru/get-zen_doc/3337090/pub_5fb822f6ffe1de7f5cef8336_5fb825dc572b862575b249fa/scale_1200" alt="">
            <div class="lifehack">
                <p>Лайфхак: Автомобиль так же можно дешево купить на рынке с рук. Авторынок находится на севере города (розовый значок).</p>
            </div>
